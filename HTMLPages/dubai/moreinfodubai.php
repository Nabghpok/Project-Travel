                <p>
                    Dubai, city and capital of the emirate of Dubai, one of the wealthiest of the seven 
                    emirates that constitute the federation of the United Arab Emirates, which was created
                     in 1971 following independence from Great Britain. There are several theories about 
                     the origin of the name Dubai. One associates it with the daba—a type of locust that 
                     infests the area—while another holds that it refers to a market that existed near the
                      city. In recent years Dubai has been compared to Singapore and Hong Kong and is often
                       regarded as the Middle East’s premier entrepôt. Area 13.5 square miles (35 square km).
                        Pop. (2017 est.) 2,919,178.


                 </p>
                 <p> 
                    Dubai straddles a natural inlet called Dubai Creek on the southern shores of the Persian Gulf.
                    \ For more than a century, the area was Dubai’s centre, because of the early city’s reliance on
                     fishing, pearl diving, and maritime trade. Lining the creek are the oldest buildings in Dubai, 
                     most of which date from the 1960s and are rarely more than two stories in height. In the 
                     Bastakiyyah quarter, on the western shore of the creek, some much older buildings have been 
                     restored, and many of these feature the distinctive wind tower design that was imported by 
                     Persian merchants early in the 20th century.

                <p>
                    The new city centre is a string of skyscrapers lining Sheikh Zayed Road. Notable among 
                    these are the Emirates Towers, which were built in the late 1990s and early 2000s 
                    and which house a hotel and government offices. Close to Sheikh Zayed Road is the 
                    Dubai International Financial Centre, housed in a futuristic arch-shaped building, and
                     the Burj Khalifa, which at the time of its official opening in 2010 was the world’s 
                     tallest building; it was named after the president of the United Arab Emirates and emir
                      of Abu Dhabi, Khalifa ibn Zayed Al Nahyan. To the west of the skyscrapers lie several 
                      affluent suburbs, most of which house substantial villas. On their periphery lies the 
                      Burj al-ʿArab, a giant sail-shaped tower which is home to a luxury hotel. Farther west 
                      are new clusters of skyscrapers surrounding a man-made marina and several man-made lakes.


                </p>
                <P>
                    Dubai’s population has grown steadily over the past two centuries, from just a few 
                    thousand local inhabitants to well over two million. Most of the early population
                     increases were due to merchants from neighbouring countries choosing to relocate to
                      Dubai’s business-friendly environment. In the later 20th century the city’s construction
                       boom led to a huge increase in the number of South Asian labourers and an influx of 
                       skilled expatriates from all over the world who play an important role in Dubai’s
                        multi-sector economy. Expatriates in the city vastly outnumber native Emiratis. With
                         the exception of the labourers, who are housed in work camps beyond the city limits,
                          expatriates of various nationalities are spread across Dubai. Although Arabic is 
                          the official language, English is, in practice, the lingua franca.
                </P>

