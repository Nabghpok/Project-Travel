                <P>
                    Spain is one of the richest countries in terms of biodiversity in the world and there are multiple options for learning 
                    about it in a responsible way. Its strategic location between two continents, its different climates, the variety of the 
                    relief and geography of its territory, with almost 8,000 kilometres of coasts and two archipelagos, explain this to a 
                    large extent. Spain’s wildlife includes species typical of Europe, the Mediterranean, Africa and the alpine regions, 
                    as well as numerous endemic species on the islands and mountain regions. In total, it is estimated that there are around 
                    70,000 animal species. The wolf, brown bear, mountain cat, Iberian lynx, deer or wild boar are just some of the most iconic
                     mammals. There are also numerous species of birds of prey, such as the imperial eagle, the bearded vulture, the black 
                     vulture, the peregrine falcon, the eagle owl and the goshawk. Spain is also an important stopping-off point for flocks
                      of migratory birds on their journey to and from Europe and Africa. Did you know that Spain is one of the best places 
