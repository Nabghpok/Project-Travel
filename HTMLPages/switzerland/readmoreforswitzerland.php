                <p>
                    In the small country of Switzerland, there is also a distinction between the culture in
                     the mountains and the culture on the central plateau. This means that the life in a 
                     mountain valley is different from that in the big city. Thus, over time and with the 
                      in tourism, some local customs have achieved national fame.
                 </p>
                 <p> 
                    Therefore, nowadays Switzerland can boast with highly preserved and living traditions, 
                    as well as local customs. The Swiss Alps have played a major role in creating the history
                     and cultural diversity of Switzerland.This country is the perfect choice for the lovers
                      of all kinds of arts. The cultural heritage of Switzerland is assembled in more than
                       600 museums, of which the Swiss people are rightly proud. The best known are the Swiss
                        land-museum in Zurich, the Art Museum in Basel, also the Transport Museum in Lucerne,
                         the Olympic Museum in Lausanne or the Red Cross Museum in Geneva. Each of these
                          unique museums combines entertainment, fun and some useful information.
                </p>
                <p>
                    Switzerland’s culture is also famous for the arts and crafts: mainly, watchmaking. The
                     industry of Swiss watches gained a respectable reputation during the past decades. 
                     The International Clock Museum in La-Chaux-de-Fond presents a vast collection of 4500 
                     timepieces including more than 700 wall clocks. Although there might be noise coming from
                      the ticking of these clocks, this museum is a relaxing place to visit.Swiss culture
                       also represents the culinary of Switzerland, which surely makes it a real-life heaven
                        for foodies. One can taste a huge number of national dishes as well as the regional 
                        specialties regardless of the canton they are visiting.
                </p>
                <P>
                    While talking about the Swiss culture, one needs to consider that they keep old-aged 
                    traditions not for the sake of tourism. They do keep those traditions alive with pride.
                     They perform folk music and dances, wear ethnic dresses and to an extent may seem too 
                     conservative. Switzerland has an enthusiastic music scene spread all over the cantons. 
                     Alpine folk music blossomed in unwritten form of skills and compositions. It passed
                      from generation to generation during decades and even centuries.
                </P>
                <p>
                    Swiss folk music is generally lively dance music. The most popular musical instruments
                     are accordion (Schwyzeroergeli), violin, bass violin, clarinet dulcimer (Jew’s harp). 
                     Of course, in this line we should not forget about alphorn, which formerly was also used
                      as a signaling instrument among the herdsmen. As a rule, every village has at least a 
                      choir or a traditional music band.Switzerland, with its cultural life and traditions,
                       attracts more and more tourists every year.
                </p>
        
